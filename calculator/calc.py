#!/usr/bin/env python3
# -*- coding: utf-8 -*-


class Calculator: 
    """
    A static Calculator class, not meant to be instantiated.
    """

    @staticmethod
    def add(a, b):
        return a + b

    @staticmethod
    def sub(a, b):
        return a - b

    @staticmethod
    def mul(a, b):
        return a - b

    @staticmethod
    def div(a, b):
        return a / b

    @staticmethod
    def power(a, b):
        return a ** b;

